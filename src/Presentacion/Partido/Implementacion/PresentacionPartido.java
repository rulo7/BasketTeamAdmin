    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    	try{
    	    TransferPartido trPartido = new TransferPartido();
    	    int hora=Integer.parseInt(this.jTextFieldHora.getText());
    	    int min=Integer.parseInt(this.jTextFieldMinuto.getText());
    	    int dia=Integer.parseInt(this.jTextFieldDia.getText());
    	    int mes=Integer.parseInt(this.jTextFieldMes.getText());
    	    int anyo=Integer.parseInt(this.jTextFieldAnyo.getText());
    	    DateTime t=new DateTime(anyo, mes, dia, hora, min);
    	    
    	    trPartido.setFechaYhora(t);
    	    trPartido.setLugar(this.jTextFieldLugar.getText());
    	    trPartido.setId(-1);
    	    trPartido.setLocalVisitante(this.jRadioButton1.isSelected());
    	    trPartido.setNumJornada(Integer.parseInt(this.jTextFieldNJornada.getText()));
    	    trPartido.setRival(this.jTextFieldRival.getText());
    	    setVisible(false);
    	    Controlador.getInstancia().accion(20, trPartido);
    	}
    	catch(NumberFormatException e){JOptionPane.showMessageDialog(null, "Campo/s introducido/s erroneamente", "Error", JOptionPane.ERROR_MESSAGE);}
    
    }//GEN-LAST:event_jButton1ActionPerformed

