    private void buttonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCrearActionPerformed
        // TODO add your handling code here:
    	try{
    	    TransferJugador trJugador = new TransferJugador(-1);
    	    trJugador.setApellidos(this.textFieldApellidos.getText());
    	    trJugador.setDNI(this.textFieldDNI.getText());
    	    trJugador.setDorsal(Integer.parseInt(this.textFieldDorsal.getText()));
    	    trJugador.setEstatura(Double.parseDouble(this.textFieldEstatura.getText()));
    	    trJugador.setNacionalidad(this.jComboBoxNacionalidad.getSelectedItem()+"");
    	    trJugador.setNombre(this.textFieldNombre.getText());
    	    trJugador.setPeso(Double.parseDouble(this.textFieldPeso.getText()));
    	    trJugador.setPosicionPrincipal(this.textFieldPrincipal.getText());
    	    trJugador.setPosicionSecundaria(this.textFieldSecundaria.getText());
    	    trJugador.setResidencia(this.textFieldResidencia.getText());
    	    trJugador.setTelefono(Integer.parseInt(this.textFieldTelefono.getText()));
    	    trJugador.setFechaNacimiento(new Date(Integer.parseInt(textFieldAnyo.getText()),Integer.parseInt(textFieldMes.getText()),Integer.parseInt(textFieldDia.getText())));
    	    //trJugador.setIdEquipo(trEquipo.getId());
    	    trJugador.setCorreo(this.textFieldCorreo.getText());
    	    setVisible(false);
    	    Controlador.getInstancia().accion(10, trJugador);
    	}
    	catch(NumberFormatException e){JOptionPane.showMessageDialog(null, "Campo/s introducido/s erroneamente", "Error", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_buttonCrearActionPerformed

